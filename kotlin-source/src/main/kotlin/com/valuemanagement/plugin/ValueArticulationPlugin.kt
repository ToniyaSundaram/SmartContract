package com.valuemanagement.plugin

import com.valuemanagement.api.ValueArticulationApi
import net.corda.core.messaging.CordaRPCOps
import net.corda.webserver.services.WebServerPluginRegistry
import java.util.function.Function

class ValueArticulationPlugin : WebServerPluginRegistry {
    /**
     * A list of classes that expose web APIs.
     */
    override val webApis: List<Function<CordaRPCOps, out Any>> = listOf(element = Function(::ValueArticulationApi))



    /**
     * A list of directories in the resources directory that will be served by Jetty under /web.
     */
    override val staticServeDirs: Map<String, String> = mapOf(
            // This will serve the exampleWeb directory in resources to /web/template
            "template" to javaClass.classLoader.getResource("exampleWeb").toExternalForm()
    )
}